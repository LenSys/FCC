# Counting and summing loops
# (see https://books.trinket.io/pfe/05-iterations.html)

count = 0
for itervar in [3, 41, 12, 9, 74, 15]:
    count = count + 1
print('Count: ', count)



# Another similar loop that computes the total of a set of numbers is as follows:

total = 0
for itervar in [3, 41, 12, 9, 74, 15]:
    total = total + itervar
print('Total: ', total)
