# For loop
# (see https://books.trinket.io/pfe/05-iterations.html)

friends = ['Joseph', 'Glenn', 'Sally']
for friend in friends:
    print('Happy New Year:', friend)
print('Done!')

# In Python terms, the variable friends is a list of three strings and the for loop goes through the list 
# and executes the body once for each of the three strings in the list resulting in this output:

# Happy New Year: Joseph
# Happy New Year: Glenn
# Happy New Year: Sally
# Done!